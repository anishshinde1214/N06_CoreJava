package org.tns.operator;

public class LogicalOperator {
       @SuppressWarnings("unused")
	public static void main (String[] args) {
    	   System.out.println(31!=7 && 3>=5);
    	   System.out.println(31!=7 || 3>=5);
    	   System.out.println(!(31!=7 && 3>=5));

    	   
       }
}
